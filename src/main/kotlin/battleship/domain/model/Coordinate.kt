package battleship.domain.model

/**
 * =============================================================================================
 * Координата клетки игрового поля 10×10.
 *
 * Система координат:
 * - Строки: 0-9 (соответствуют буквам A-J).
 * - Столбцы: 0-9 (соответствуют цифрам 1-10).
 *
 * Инварианты:
 * Строка и столбец всегда в диапазоне [0..9] - проверяется в `init`.
 *
 * Использование:
 * Используется для адресации клеток на [Board], описания сегментов [Ship],
 * а также в ботах для перебора соседних клеток.
 *
 * @param row строка (0 = A, 9 = J)
 * @param col столбец (0 = 1, 9 = 10)
 * @throws IllegalArgumentException если координата выходит за пределы поля
 * =============================================================================================
 */
data class Coordinate(val row: Int, val col: Int) {

    init {
        require(row in 0..9) { "Строка должна быть 0-9, получено: $row" }
        require(col in 0..9) { "Столбец должен быть 0-9, получено: $col" }
    }

    /**
     * ---------------------------------------------------------------------------------------------
     * Человекочитаемое представление координаты.
     *
     * Примеры: A1 (row=0, col=0), J10 (row=9, col=9).
     *
     * @return строка вида "A1", "J10"
     * ---------------------------------------------------------------------------------------------
     */
    fun toDisplayString(): String = "${'A' + row}${col + 1}"

    companion object {
        /**
         * ---------------------------------------------------------------------------------------------
         * Безопасное создание координаты.
         *
         * Возвращает `null`, если координата вне поля.
         * Используется в алгоритмах бота ([SmartBot], [RandomShipPlacer])
         * для фильтрации граничных клеток при переборе соседей.
         *
         * @param row строка
         * @param col столбец
         * @return [Coordinate] или `null`, если координата вне диапазона 0..9
         * ---------------------------------------------------------------------------------------------
         */
        fun ofOrNull(row: Int, col: Int): Coordinate? =
            if (row in 0..9 && col in 0..9) Coordinate(row, col) else null

        /**
         * ---------------------------------------------------------------------------------------------
         * Разбор строкового представления координаты.
         *
         * Поддерживаемые форматы: "A1", "J10", "b5" (регистр не важен).
         *
         * @param input строка вида "A1".."J10"
         * @return [Coordinate] или `null`, если формат неверен или координата вне поля
         * ---------------------------------------------------------------------------------------------
         */
        fun fromString(input: String): Coordinate? {
            val s = input.trim().uppercase()
            if (s.length !in 2..3) return null
            val row = s[0] - 'A'
            val col = s.substring(1).toIntOrNull()?.minus(1) ?: return null
            return ofOrNull(row, col)
        }
    }
}