package battleship.domain.model

/**
 * =============================================================================================
 * Игрок.
 *
 * Представляет участника партии «Морского боя».
 * Идентифицируется уникальным [id], имеет отображаемое [name]. Может быть как человеком,
 * так и ботом — для бота используется специальный префикс `BOT-` в id.
 *
 * Использование:
 * - Создаётся в [ConsoleUI] при добавлении нового игрока.
 * - Хранится в [PlayerRepository] для повторного использования.
 * - Связывается с [EloRating] для отслеживания рейтинга.
 * - Участвует в [Game] как `player1` или `player2`.
 *
 * @param id уникальный идентификатор (UUID для людей, "BOT-{uuid}" для ботов)
 * @param name отображаемое имя игрока
 * =============================================================================================
 */
data class Player(
    val id: String,
    val name: String
) {
    /**
     * ---------------------------------------------------------------------------------------------
     * Строковое представление игрока - его имя.
     *
     * Используется при выводе в консоль, логах и истории ходов.
     *
     * @return имя игрока
     * ---------------------------------------------------------------------------------------------
     */
    override fun toString(): String = name
}
