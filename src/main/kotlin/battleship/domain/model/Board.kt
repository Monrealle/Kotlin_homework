package battleship.domain.model

/**
 * =============================================================================================
 * Игровое поле 10×10.
 *
 * Хранит состояние всех клеток и список размещённых кораблей.
 * Валидация расстановки делегируется [ShipPlacementValidator] -
 * класс `Board` предполагает, что корабли уже проверены перед вызовом [placeShips].
 *
 * Структура:
 * - [grid] - карта `Coordinate -> CellState`, изначально все клетки `EMPTY`.
 * - [ships] - список кораблей, размещённых на поле.
 * - [owner] - игрок, которому принадлежит доска.
 *
 * @param owner игрок-владелец доски
 * =============================================================================================
 */
class Board(val owner: Player) {

    /**
     * ---------------------------------------------------------------------------------------------
     * Сетка 10×10: координата -> состояние клетки.
     * Инициализируется пустым полем (все клетки [CellState.EMPTY]).
     * ---------------------------------------------------------------------------------------------
     */
    val grid: MutableMap<Coordinate, CellState> = buildMap {
        for (r in 0..9) for (c in 0..9) put(Coordinate(r, c), CellState.EMPTY)
    }.toMutableMap()

    /**
     * ---------------------------------------------------------------------------------------------
     * Список кораблей, размещённых на поле.
     * ---------------------------------------------------------------------------------------------
     */
    val ships: MutableList<Ship> = mutableListOf()

    /**
     * ---------------------------------------------------------------------------------------------
     * Размещает корабли на поле.
     *
     * Предполагается, что корабли уже прошли валидацию через [ShipPlacementValidator].
     * Очищает предыдущую расстановку и заполняет сетку заново:
     * сначала все клетки становятся `EMPTY`, затем сегменты кораблей - `SHIP`.
     *
     * @param newShips список кораблей для размещения
     * ---------------------------------------------------------------------------------------------
     */
    fun placeShips(newShips: List<Ship>) {
        ships.clear()
        ships.addAll(newShips)
        for (r in 0..9) for (c in 0..9) grid[Coordinate(r, c)] = CellState.EMPTY
        for (ship in newShips) {
            for (seg in ship.segments) grid[seg] = CellState.SHIP
        }
    }

    /**
     * ---------------------------------------------------------------------------------------------
     * Обрабатывает выстрел по клетке [coord].
     *
     * ## Логика
     * - Если в клетке `SHIP` -> меняет состояние на `HIT`,
     *   то проверяет, потоплен ли корабль и все ли корабли потоплены.
     * - Если `EMPTY` -> меняет на `MISS`.
     * - Если клетка уже атакована (`HIT` или `MISS`) - выбрасывает исключение.
     *
     * @param coord координата выстрела
     * @return результат: [ShotResult.MISS], [ShotResult.HIT], [ShotResult.SUNK] или [ShotResult.WIN]
     * @throws IllegalStateException если клетка уже атакована
     * ---------------------------------------------------------------------------------------------
     */
    fun receiveShot(coord: Coordinate): ShotResult {
        return when (grid[coord]) {
            CellState.SHIP -> {
                grid[coord] = CellState.HIT
                val currentHits = hitCells()
                val ship = ships.first { coord in it.segments }
                when {
                    ship.isSunk(currentHits) && allShipsSunk() -> ShotResult.WIN
                    ship.isSunk(currentHits) -> ShotResult.SUNK
                    else -> ShotResult.HIT
                }
            }
            CellState.EMPTY -> {
                grid[coord] = CellState.MISS
                ShotResult.MISS
            }
            else -> error("Клетка $coord уже была атакована (состояние: ${grid[coord]})")
        }
    }

    /**
     * ---------------------------------------------------------------------------------------------
     * Проверяет, потоплены ли все корабли на поле.
     *
     * @return `true`, если все корабли полностью состоят из попаданий
     * ---------------------------------------------------------------------------------------------
     */
    fun allShipsSunk(): Boolean {
        val hits = hitCells()
        return ships.all { it.isSunk(hits) }
    }

    /**
     * ---------------------------------------------------------------------------------------------
     * Возвращает множество всех атакованных клеток (HIT + MISS).
     * Используется ботами для исключения уже обстрелянных клеток.
     *
     * @return множество координат, по которым уже стреляли
     * ---------------------------------------------------------------------------------------------
     */
    fun attackedCells(): Set<Coordinate> =
        grid.entries.filter { it.value == CellState.HIT || it.value == CellState.MISS }
            .map { it.key }.toSet()

    /**
     * ---------------------------------------------------------------------------------------------
     * Возвращает множество клеток с попаданиями (только HIT).
     * Используется для проверки потопления кораблей.
     *
     * @return множество координат с состоянием HIT
     * ---------------------------------------------------------------------------------------------
     */
    fun hitCells(): Set<Coordinate> =
        grid.entries.filter { it.value == CellState.HIT }.map { it.key }.toSet()

    /**
     * ---------------------------------------------------------------------------------------------
     * Возвращает список клеток, по которым ещё не стреляли.
     * Используется ботами для выбора следующей цели.
     *
     * @return список координат с состоянием EMPTY или SHIP
     * ---------------------------------------------------------------------------------------------
     */
    fun unattackedCells(): List<Coordinate> =
        grid.entries.filter { it.value == CellState.EMPTY || it.value == CellState.SHIP }
            .map { it.key }

    /**
     * ---------------------------------------------------------------------------------------------
     * Отладочное представление доски.
     *
     * @return строка вида "Board of Player 1"
     * ---------------------------------------------------------------------------------------------
     */
    override fun toString(): String = "Board of ${owner.name}"
}
