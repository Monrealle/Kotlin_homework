package battleship.domain.model

/**
 * =============================================================================================
 * Корабль.
 *
 * Представляет один корабль на игровом поле.
 * Состоит из типа и списка клеток-сегментов, которые он занимает.
 * Не хранит состояние попаданий - попадания отслеживаются классом [Board]
 * через состояния клеток.
 *
 * Инварианты:
 * - Количество сегментов должно соответствовать размеру [type].
 * - Список сегментов не может быть пустым.
 *
 * Использование:
 * - Размещается на [Board] через [Board.placeShips].
 * - Проверяется валидатором [ShipPlacementValidator].
 * - Используется ботами для анализа попаданий и потопления.
 *
 * @param type тип корабля (определяет размер)
 * @param segments список клеток, занимаемых кораблём
 * @throws IllegalArgumentException если количество сегментов не соответствует типу или список пуст
 * =============================================================================================
 */
data class Ship(
    val type: ShipType,
    val segments: List<Coordinate>
) {
    init {
        require(segments.size == type.size) {
            "Корабль ${type.name} должен содержать ${type.size} сегментов, получено: ${segments.size}"
        }
        require(segments.isNotEmpty()) { "Список сегментов не может быть пустым" }
    }

    /**
     * ---------------------------------------------------------------------------------------------
     * Проверяет, потоплен ли корабль.
     *
     * Корабль считается потопленным, если все его сегменты есть
     * в множестве переданных попаданий.
     *
     * @param hits множество координат с попаданиями (обычно из [Board.hitCells])
     * @return `true`, если все сегменты поражены
     * ---------------------------------------------------------------------------------------------
     */
    fun isSunk(hits: Set<Coordinate>): Boolean = segments.all { it in hits }

    /**
     * ---------------------------------------------------------------------------------------------
     * Проверяет, пересекается ли этот корабль с другим.
     *
     * Пересечение - хотя бы одна общая клетка у двух кораблей.
     * Используется в валидации расстановки.
     *
     * @param other другой корабль
     * @return `true`, если корабли занимают одну и ту же клетку
     * ---------------------------------------------------------------------------------------------
     */
    fun overlapsWith(other: Ship): Boolean =
        segments.any { it in other.segments }

    /**
     * ---------------------------------------------------------------------------------------------
     * Проверяет, соприкасается ли этот корабль с другим.
     *
     * Соприкосновением считается любое соседство, включая диагонали (расстояние Чебышёва ≤ 1).
     * Используется в валидации расстановки - по правилам корабли не могут соприкасаться.
     *
     * @param other другой корабль
     * @return `true`, если корабли находятся на соседних клетках (включая диагонали)
     * ---------------------------------------------------------------------------------------------
     */
    fun isAdjacentTo(other: Ship): Boolean =
        segments.any { seg ->
            other.segments.any { otherSeg ->
                maxOf(
                    kotlin.math.abs(seg.row - otherSeg.row),
                    kotlin.math.abs(seg.col - otherSeg.col)
                ) <= 1
            }
        }
}
