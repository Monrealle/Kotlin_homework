package battleship.domain.model

/**
 * =============================================================================================
 * Тип корабля (Domain-слой).
 *
 * Определяет размер корабля и его отображаемое имя.
 * Используется при расстановке флота, валидации и генерации случайных кораблей.
 *
 * Состав классического флота:
 * - [BATTLESHIP] — 1 шт. (4 клетки)
 * - [CRUISER]    — 2 шт. (3 клетки)
 * - [DESTROYER]  — 3 шт. (2 клетки)
 * - [BOAT]       — 4 шт. (1 клетка)
 *
 * @param size длина корабля в клетках
 * @param displayName русскоязычное название для вывода пользователю
 * =============================================================================================
 */
enum class ShipType(val size: Int, val displayName: String) {
    BATTLESHIP(4, "Линкор"),               /* Линкор (4 клетки).  */
    CRUISER(3, "Крейсер"),                 /* Крейсер (3 клетки). */
    DESTROYER(2, "Эсминец"),               /* Эсминец (2 клетки). */
    BOAT(1, "Катер")                       /* Катер (1 клетка).   */
}
