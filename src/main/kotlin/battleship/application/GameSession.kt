package battleship.application

import battleship.domain.model.*

/**
 * =============================================================================================
 * Координатор игровой сессии (Application-слой).
 *
 * Определяет контракт управления жизненным циклом одной партии:
 * создание, расстановку кораблей, выполнение ходов и получение состояния.
 * Реализация должна поддерживать игру как двух людей, так и человека против бота.
 *
 * @see GameSessionImpl
 * =============================================================================================
 */
interface GameSession {

    /**
     * ---------------------------------------------------------------------------------------------
     * Начать новую партию между двумя игроками.
     *
     * @param p1 первый игрок (обычно человек)
     * @param p2 второй игрок (человек или бот)
     * ---------------------------------------------------------------------------------------------
     */
    fun startGame(p1: Player, p2: Player)

    /**
     * ---------------------------------------------------------------------------------------------
     * Расставить корабли для указанного игрока.
     * Валидация расстановки выполняется реализацией через [ShipPlacementValidator].
     *
     * @param player игрок, расставляющий корабли
     * @param ships  список кораблей (уже сформированный)
     * @return результат валидации: успех или список ошибок
     * ---------------------------------------------------------------------------------------------
     */
    fun placeShips(player: Player, ships: List<Ship>): ValidationResult

    /**
     * ---------------------------------------------------------------------------------------------
     * Выполнить ход игрока.
     *
     * Если игра против бота, после хода игрока бот может ответить автоматически
     * (зависит от реализации).
     *
     * @param player игрок, делающий ход (должен совпадать с текущим игроком)
     * @param coord  координата выстрела
     * @return объект [Move], описывающий совершённый ход игрока
     * @throws IllegalArgumentException если ход нелегален (очерёдность, повтор, неверный статус)
     * ---------------------------------------------------------------------------------------------
     */
    fun makeMove(player: Player, coord: Coordinate): Move

    /**
     * ---------------------------------------------------------------------------------------------
     * Получить текущее состояние партии.
     *
     * @return живой объект [Game]; все изменения в сессии сразу отражаются в нём
     * ---------------------------------------------------------------------------------------------
     */
    fun getGame(): Game
}
